CREATE TABLE person
(
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(255) NOT NULL,
    email           VARCHAR(255) NOT NULL,
    password        VARCHAR(255),
    role            CHAR(10) DEFAULT 'user',
    created_at      TIMESTAMPTZ,
    changed_at      TIMESTAMPTZ,
    gender          CHAR(10),
    age             INT,
    weight          REAL,
    height          INT,
    steps_by_day    INT,
    fitness_by_day  INT,
    aerobics_by_day INT
);


